            <div class='col col_6 col_offset_3 last qa'>
                <p><strong>To categorize Tunde Olaniran</strong> is nearly impossible. His music lives somewhere in the middle of R&B, pop, dance and rap, and is hung just off center enough to come across as something that’s never been done. The Flint-based artist flows effortlessly between singing and rapping, usually in the same song; Missy Elliott-style pacing with serious danceability punctuated by a four-octave range.</p>
                <p>And that range is something to witness. During our photo shoot, the mood in the studio altogether changed when Robyn’s “Dancing on My Own” was played (his request). Mere notes into his sing-along, not a hair in the room lay flat.</p>
                <p>Olaniran’s music is a trip and he is your entertaining, well-traveled, socially conscious guide. We had a chance to sit down and talk about his process, his outlook and what it is that makes a dirty synth so enjoyable.</p>
            </div>
            <div class='col col_12 last'>
                <img src='/img/content/2013/04/music/tunde_002.jpg' />
