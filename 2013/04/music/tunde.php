            <div class='col col_6 col_offset_3 last qa'>
                <p><strong>LD</strong> You describe yourself as “a cultural warrior with a 4-octave range and a penchant for dirty synths.” </p>
                <p class='a'><strong>TUNDE</strong> Yes [laughs]. That came from Twitter when I needed a bio.</p>

                <p><strong>LD</strong> I want to talk about each of those things. First, what do you mean by cultural warrior?</p>
                <p class='a'><strong>TUNDE</strong> I think there are a lot of different issues that polarize people but at the same time there’s a lot of things that people don’t even think about exploring. So I feel like— especially as someone whose background is anthropology and someone who is into activism on a certain level— I’m battling almost a tide of ignorance around not just diversity in that really static sense, but how dynamic our culture is and people’s worlds and lives are. We tend to ignore that and over-generalize and just not be curious about other people, other experiences. So for me, that’s the battle: trying to get people to connect to themselves, or their space, or place, or community. We have so much capacity for that in our brains but we don’t really use it. So that’s kind of what that’s about. But then it’s also just battling ignorance around issues like sexual health, gender identity, poor people’s rights.</p>

                <p><strong>LD</strong> Let’s talk about that four-octave range.</p>
                <p class='a'><strong>TUNDE</strong> Yes, I tested it. I took a music class in college; it was almost like the basics of music. It was at the time I was doing Chicago Idol and asked the teacher how to test an octave range. And she tested it and it was four octaves.</p>
            </div>
            <div class='col col_12 last'>
                <img src='/img/content/2013/04/music/tunde_003.jpg' />
            </div>
            
            <div class='col col_6 col_offset_3 last qa'>
                <p><strong>LD</strong> How did you learn to sing like that?</p>
                <p class='a'><strong>TUNDE</strong> From mimicking people, and being in a rock band. Being in a band that where you do three-hour shows and also having certain music idols or vocalists styles that I love and trying to stretch to sing along with those people. Those two things give you a stronger voice. </p>

                <p><strong>LD</strong> Who are some of those idols?</p>
                <p class='a'><strong>TUNDE</strong> Paula Cole—Paula Cole is the shit—and I always really liked Lauryn Hill’s tone. And Sinead O’Connor has a really amazing voice.</p>

                <p><strong>LD</strong> Dirty synths. Let’s talk about them. Who doesn’t love a dirty synth?</p>
                <p class='a'><strong>TUNDE</strong> I feel lucky to be alive right now, because I feel like we’re in another cool age of synth music. It’s so creative and really emotional now. And that’s the part I love. For some people, that bass guitar sound is what gets them, or some people love a good drum solo. And I don’t always universally connect to that but I will always respond to a great synth line. Like James Blake’s new song, “Retrograde” is amazing. It comes in at the bridge and it’s like… it’s crazy. I think for me, the synth gets the most emotional response than from any other instrument. I prefer to record analog synths. I don’t do a lot of programming, so it feels like a real instrument to me, and it feels tangible.</p>
            </div>
            <div class='col col_12 last'>
                <img src='/img/content/2013/04/music/tunde_004.jpg' />
            </div>
            
            <div class='col col_6 col_offset_3 last qa'>
                <p><strong>LD</strong> Do you play any instruments?</p>
                <p class='a'><strong>TUNDE</strong> No, pshhh [laughs]. Someone gave me some advice about being good at one or two things and not trying to be good at everything. That’s one of the reasons why I still work with an engineer. I’m not saying I’d never want to learn an instrument, but I’d rather be a better singer, or have a better stage show or work on choreography and imagery because that’s what appeals to me, and what I feel like I can do well.</p>

                <p><strong>LD</strong> About the choreography—you put on quite a show. Do you do your own choreography?</p>
                <p class='a'><strong>TUNDE</strong> Yeah. My feeling is that I want to make sure people enjoy themselves. Even it’s just five bucks [to get into a show]. Because going to a show, sometimes it’s a struggle and I appreciate people who drove 15 minutes, or tried to find parking, or tried to convince their friends to come out, or put up with knowing they’d see their ex-boyfriend at that bar. So I want it to be memorable and fun. And I want to have fun, too. So yeah, I choreograph it and the dancers I work with are super dope. They’re classically trained and they pick it up easily. Watching them inspires me too.</p>
            </div>
            <div class='col col_12 last'>
                <img src='/img/content/2013/04/music/tunde_005.jpg' />
            </div>
            
            <div class='col col_6 col_offset_3 last qa'>
                <p><strong>LD</strong> Do you have formal dance training?</p>
                <p class='a'><strong>TUNDE</strong> No, just at the club or whatever… But I love dance and I’ll watch choreography videos all night on YouTube. This has been a really fun new chapter for me to explore.</p>

                <p><strong>LD</strong> You’ve lived in Flint, England, Germany and Nigeria.</p>
                <p class='a'><strong>TUNDE</strong> Yes, those are the main places.</p>

                <p><strong>LD</strong> That’s a very diverse set of geography. Do you think each of those places is reflected in your art?</p>
                <p class='a'><strong>TUNDE</strong> If you live in a little diaspora, it’s a little pocket of that culture, so it’s not like you ever completely leave it. So even in London, almost everyone I was around was African. In living in all those places, I think the one thing that stuck with me is that there are no absolutes. That’s the main point of how I work as an artist and how I create stuff and how I think about lyrics and imagery. </p>

                <p class='a'>I also spent a lot of time by myself because I’m an only child, and you’re in places where you are sort of alien. When I was in Germany, I remember meeting kids who had never seen black people before and they’d touch your skin. So feeling really alien, not knowing the language, I think makes you more introspective. So some of my lyrics and music represents that.</p>

                <p><strong>LD</strong> Your lyrics come off as pretty personal. Is that the case? Or do you grab stuff from what’s happening around you? </p>
                <p class='a'><strong>TUNDE</strong> I do grab stuff… some people have more interesting lives than me. So if I see something that happens to someone else and think that’s really interesting or, how they reacted to it or how it affected them. Sometimes I don’t put a lot of thought into the lyrics. I just write. It’s a lot of whatever the music is making me feel and I sing what words feel right and they end up coming together and when I go back around I find meaning in it. You work with session songwriters and they’re so serious about their craft. For me—I’m not Taylor Swift. It’s not that serious, I’m not trying to get a Grammy with it. My process isn’t like theirs. That’s something I’m willing to learn, but that’s not how it is right now.</p>
                
            </div>
            <div class='col col_12 last'>
                <img src='/img/content/2013/04/music/tunde_006.jpg' />
            </div>
            
            <div class='col col_6 col_offset_3 last qa'>
                <p><strong>LD</strong> Since you don’t play instruments, how do you usually write your songs?</p>
                <p class='a'><strong>TUNDE</strong> I produce a track, I’ll have a cool sample, or a drum beat, and sequence it out. With “Brown Boy,” there was [a sample of] this old grandfather who was retiring from the police force and gave this speech, and then there was this [sample of] kindergarten-aged Chinese kids doing drills outdoors. So those two sounds, I looped them and figured out the pattern, and that made it click and I built everything around that. So I produce it and write to it. It’s fun. </p>

                <p><strong>LD</strong> What is your favorite song of your own?</p>
                <p class='a'><strong>TUNDE</strong> One that isn’t out yet. It’s with a guy names James Linck and we wrote it a month ago. It’s spooky and has sort of an old Neptunes sound to it. It’s going to be part of a mix tape I’m doing for the summer. I listen to that song right now for pleasure, not even to critique it. It’s a really cool song.</p>

                <p><strong>LD</strong> When will that be out?</p>
                <p class='a'><strong>TUNDE</strong> I’m recording it in April. There’s also a song with Passalacqua on the album. I’m hoping for summer time, June. It’s party, summery music.</p>

                <p><strong>LD</strong> What’s the name of it?</p>
                <p class='a'><strong>TUNDE</strong> I don’t have it yet! I want to do something really trifling and name it something like, “#Wratchetry.” Just something ridiculous.</p>

                <p><strong>LD</strong> If you could collaborate with any artist, who would it be?</p>
                <p class='a'><strong>TUNDE</strong> Locally, I want to work with Monica Blair. She’s just a really cool person and she’s so talented. She’s just raw, you feel really affected when she sings. She’s also a really cool rapper. I’d love to do something with her that’s a hybrid of that. And then dream, something that will probably never happen would be Switch, who was a producer that worked in Major Lazer. He did all of Santigold’s stuff and does really amazing production. I’d love to work with him and do a whole album. Maybe if I win the lottery.</p>
