                <p><b>The Fencemen</b>: We played a couple shows with these dudes last month-ish and they were great. Heavy grooves.</p>
                <p><b>Ciccarelli</b>: Ciccarelli also played one of the shows with The Fencemen and Nice Hooves at the Lager House. There was a great turnout and Dave fell through the stage at that show during our set. In conclusion, Ciccarelli was great.</p>
                <p><b>Sponge</b>: To be honest I'm just looking through the lineup on the website at this point and looking for names I recognize. I had no idea Sponge was still a band—whoa! I hear these dudes on Sirius' Lithium channel sometimes. I like that channel because they play Nirvana and Soundgarden a lot. Sometimes they just play like Limp Bizkit though and it totally blows my mind because I'm pretty sure that stuff wasn't until the 2000's. Let me check the wiki…well look here, it says they started in 1994 and the first record came out in ‘98. Guess I was wrong. Still though, they specifically say "grunge" in the channel description so I maintain my position that Limp Bizkit shouldn't be heard on Lithium. So yeah, go see Sponge.</p>
                <span class='sq'>&rdquo;</span>            
