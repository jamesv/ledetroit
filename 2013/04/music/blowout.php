                <p>So Friday, Nice Hooves is playing <b>The Meatmen</b> showcase at the PNA and the bill is absolutely stacked with killer bands. So let me preface by saying I'm most excited about all of those bands but I decided to chime in on bands outside of that particular bill because why make it so easy?</p>
                <p><b>The Beggars</b>: I first saw this band in high school and it was awesome. I last saw this band a few months ago and it was still really awesome. They are awesome. They have songs like "Stop, Drop, & Rock 'n' Roll," and the singer does the splits and kinds of sweet jumps. Unlike a lot of Detroit bands that have been around for a while, these guys haven't completely transformed with all the latest indie trends like an amorphous blob of posturing aging hipsters. These dudes are rocking until they can rock no longer. Impossible to be bummed while watching this band.</p>
                <p><b>Cold Men Young</b>: These dudes were Bad Boy Entertainment for Halloween at the Crofoot! How insane is that?! They opened with Victory and I almost cried. For 20 minutes, this band took me back to my formative middle school years.</p>
