                <p>There are so many talented acts playing this year, so I'll talk about two I think everyone else should see and two that I'd like to check out for myself. Everyone should try to see <b>Alexis</b> (Grand Rapids) and <b>Odd Hours</b> (Detroit/Flint). Alexis is like if the bitchy popular high school girl from any 80s movie mixed with Jareth the Goblin King playing a keytar. They're fantasy, pop, with sexy vocals and synth. Odd Hours are like the Yeah Yeah Yeahs mixed with the dumpster monster from Mulholland Drive (in a good way). They both make an effort to put on a memorable performance, which I always appreciate.</p>
                <p>I'm most excited to see <b>Flint Eastwood</b>! I hear great things, and they just *happen* to be playing right before me during our showcase with <b>Passalacqua</b> and <b>James Linck</b> at the Loving Touch. I also want to catch <b>Autumn Wolf</b>, because I missed his set at the last FlashClash show at the Magic Bag. Seems like a classy dude.</p>
