                        <p><strong>They came together</strong> through a combination of friends, word of mouth, and Facebook in January of 2012. Stollsteimer wanted to make his side project a permanent band so he put the word out. None of the members had met, or even heard each other play, before auditions. But the stars aligned and the Hounds Below became an official thing. “The chance of it working was totally random. I’m very surprised we get along at all,” says Stollsteimer.</p>
                        <p>“It’s a crapshoot,” says Hofman. “Half of your duty is to play the songs right and be a good musician. The other half is getting along with everybody.” </p>
                        <p class='nobreak'>For a band that didn’t know one another a year ago, by the listen of it, you’d never guess. “With this band, the friendships have grown through the band, not before the band. The sound wasn’t initially a group thing, but it’s become that,” says Stollsteimer. “We morphed,” adds Hofman.</p>
                        <p>“We’re a lot more comfortable than we were, say last January,” conceded Stollsteimer. “We’re still getting to know each other on a personal level. But musically, everybody’s on the same page, which I’m very surprised at.” Given the legendary band member showdowns in rock ‘n’ roll history, one can’t blame his astonishment.</p>
