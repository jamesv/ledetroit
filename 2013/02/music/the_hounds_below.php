                    <p><strong>In the span of just 10 songs</strong> on their recent debut, <em>You Light Me Up in the Dark</em>, the mood shifts seamlessly in highs and lows, while Stollsteimer’s vocals shift just as easily between an Elvis Presley croon and Cobain-esque wailing. It’s an impressive feat that serves as a hook of its own upon first listen of the album.</p>
                    <p>But he’ll be the first to tell you that The Hounds Below isn’t a one-man show. Drummer Griffin Bastian, bassist Mathew Hofman, and guitarist Skye Thrasher hold it down with a mix of emphatic rhythms and sticky melodies. The band’s divergent influences peek out here and there, from the post-punk urgency on songs like “Since He Came to Play” and “Deathcab Song,” to the Americana-tinged riffs of the title track and “Chelsea’s Calling.” But the eclectic mix of throwbacks somehow produces something that’s very now. And, perhaps more importantly, very approachable.</p>
                    <p>“I like the purity of a good pop song,” says Stollsteimer. “I hate feeling isolated when I hear music that I don’t understand… like jazz or classical. I don’t read music, I’m not classically trained. I grew up listening to Nirvana and Pavement and Buddy Holly. I write music that’s accessible. Even if the lyrics can be deep [sometimes], people can still enjoy it. I think that’s important.”</p>
                    
